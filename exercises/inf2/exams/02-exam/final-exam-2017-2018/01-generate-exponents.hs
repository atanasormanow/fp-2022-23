-- Да се напише функция, generateExponents, която по дадени различни естествени числа k и l,
-- генерира безкрайния поток от всички числа от вида x^k * y^l, без повторения и подредени в нарастващ ред.

generateExponents k l =
  [ f | f <- [1..], x <- [1..f], y <- [1..f], x^k * y^l == f]

-- take 7 (generateExponents 2 3) -- => [1, 4, 8, 9, 16, 25, 27]